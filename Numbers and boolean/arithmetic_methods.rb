# addition operator is +
# subtraction operator is -
# multiplication operator is *
# division operator is /
# modulus operator is %
# exponent operator is **
# .next operator
p 5.next      #=> 6
# and optional is use .next(n)
p 5.next()    #=> 6

p 3 * 3       #=> 9
p 3.* 3       #=> 9

p 3 / 3       #=> 1
p 3./ 3       #=> 1
p 3.div(3)    #=> 1

p 3 % 3       #=> 0
p 3.% 3       #=> 0
p 3.modulo(3) #=> 0