puts "Juliet said 'Goodbye' to Romeo"   #=> Juliet said 'Goodbye' to Romeo
puts 'Juliet said "Goodbye" to Romeo'   #=> Juliet said "Goodbye" to Romeo
puts "Juliet said \"Goodbye\" to Romeo" #=> Juliet said "Goodbye" to Romeo
puts 'Juliet said \'Goodbye\' to Romeo' #=> Juliet said "Goodbye" to Romeo

# adding line breaks
puts "Hello\nworld"                     #=> Hello
                                        #=> world

# \t is a tab
puts "\tHello world"                    #=> 	Hello world
