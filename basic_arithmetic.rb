# Operación de suma
p 1 + 5   # 6

# Operación de resta
p 1 - 5   # -4

# Operación de multiplicación
p 1 * 5   # 5

# Operación de división
p 10 / 5  # 2
p 1 / 5   # 0 (no es exacta)
p 1.0 / 5 # 0.2 (uno o los dos deben ser float)

# Operación de exponenciación
p 2 ** 3  # 8

# Operación de modulo
p 10 % 3  # 1
p 6 % 3   # 0

# Concatenación de cadenas
p "Hola" + " mundo" # "Hola mundo"