puts "Steven Seagal"
# resultado: Steven Seagal

# p entrega información que no entrega puts
p "Steven Seagal"
# resultado: "Steven Seagal", nos dice que es un string

puts "Hi there, this is a 
big line break"
# resultado: Hi there, this is a
# big line break

p "Hi there, this is a
big line break"
# resultado: "Hi there, this is a\nbig line break"
# \n es un salto de línea

puts 5 # resultado: 5
p 5 # resultado: 5
# Son números por lo que no vemos diferencias visibles
