puts "Hello"
puts "World"
# Resultado:
# Hello
# World

# print hace una copia de puts pero no imprime el salto de línea
print "Hello"
print "World"
# Resultado: HelloWorld

# Una alternativa para generar un espacio es agregar un nuevo print con una cadena vacía
print "Hello"
print " "
print "World"
# Resultado: Hello World